package com.example.android.tourguide;

public class GovernorateItem {
    private int image;
    private String name;

    public GovernorateItem(int img, String name) {
        this.image = img;
        this.name = name;
    }
    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

}
